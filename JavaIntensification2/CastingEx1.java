package JavaIntensification2;

public class CastingEx1 {
	//How to type casting
	public static void main(String[] args) {
		double kopo09_d = 85.4;	// double type variable(kopo09_d) declaration and initialize it to 85.4
		int kopo09_score = (int)kopo09_d;	// type casting(double to integer)
		
		System.out.println("score="+kopo09_score);	// Print kopo09_score
		System.out.println("d="+kopo09_d);	// Print kopo09_d
	}

}
