package JavaIntensification2;

public class SpecialCharEx {

	// Escape character
	public static void main(String[] args) {
		System.out.println('\'');	// \' (single quotes)
		System.out.println("abc\t123\b456");	// \t (tap), \b(backspace)
		System.out.println('\n');	//	\n(new line)
		System.out.println("\"Hello\"");	//	\"(double quotes)
		System.out.println("c:\\");	//	\\(backslash)
	}

}
